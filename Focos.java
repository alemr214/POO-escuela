public class Focos {
   private String marca;
   private short numeroLeds;
   private float precio;

   public String getMarca() {
      return marca;
   }

   public short getNumeroLeds() {
      return numeroLeds;
   }

   public float getPrecio() {
      return precio;
   }

   public void setMarca(String marca) {
      this.marca = marca;
   }

   public void setNumeroLeds(short numeroLeds) {
      this.numeroLeds = numeroLeds;
   }

   public void setPrecio(float precio) {
      this.precio = precio;
   }
}
